//Drawing Block sizes (Scaling Image)
void DrawBlock(unsigned int xOff, unsigned int yOff, unsigned int size = 16, unsigned int color = 0xffffffff){
    unsigned int* pixPtr = (unsigned int*)GlobalRenderer->framebuffer->BaseAddress;
    for (unsigned long y = yOff; y < yOff + size; y++){
        for (unsigned long x = xOff; x < xOff + size; x++){
                *(unsigned int*)(pixPtr + x + (y * GlobalRenderer->framebuffer->PixelsPerScanLine)) = color;
        }
    }
}

//Main Panic
void Panic(const char* PanicMessage){
    GlobalRenderer->Clear();
    GlobalRenderer->CursorPosition = {0, 0};

    //Calculate Sizing and offsetting (Center Allignment)
    uint32_t Size = GlobalRenderer->framebuffer->Width / 120;

    uint32_t MainXOffset = (GlobalRenderer->framebuffer->Width - (Size * 54)) / 2;

    uint32_t Sub1XOffset = strlen("Kernel Panic: ") + strlen(PanicMessage);
    Sub1XOffset = (GlobalRenderer->framebuffer->Width - (8 * Sub1XOffset)) / 2;

    uint32_t Sub2XOffset = strlen("We don't know what caused it.");
    Sub2XOffset = (GlobalRenderer->framebuffer->Width - (8 * Sub2XOffset)) / 2;

    GlobalRenderer->CursorPosition.y += Size;

    for(int y = 0; y < 18; y++){
        for(int x = 0; x < 54; x++){
            if(ErrImg[y][x] == 1){
                DrawBlock(x*Size + MainXOffset, y*Size + Size, Size);
            }
        }
        GlobalRenderer->CursorPosition.y += Size;
    }

    GlobalRenderer->Next();

    GlobalRenderer->CursorPosition.x = Sub1XOffset;
    GlobalRenderer->PrintAtCursor("Kernel Panic: ");
    GlobalRenderer->PrintAtCursor(PanicMessage);
    GlobalRenderer->Next();

    GlobalRenderer->CursorPosition.x = Sub2XOffset;
    GlobalRenderer->PrintAtCursor("We don't know what caused it.");
    GlobalRenderer->Next();
}