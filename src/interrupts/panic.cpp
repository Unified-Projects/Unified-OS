//Drawing Block sizes (Scaling Image)
void DrawBlock(unsigned int xOff, unsigned int yOff, unsigned int size = 16, unsigned int color = 0xffffffff){
    unsigned int* pixPtr = (unsigned int*)GlobalRenderer->framebuffer->BaseAddress;
    for (unsigned long y = yOff; y < yOff + 16; y++){
        for (unsigned long x = xOff; x < xOff+16; x++){
                *(unsigned int*)(pixPtr + x + (y * GlobalRenderer->framebuffer->PixelsPerScanLine)) = color;
        }
    }
}

//Main Panic
void Panic(const char* PanicMessage){
    GlobalRenderer->Clear();
    GlobalRenderer->CursorPosition = {0, 0};

    for(int y = 0; y < 18; y++){
        for(int x = 0; x < 54; x++){
            if(ErrImg[y][x] == 1){
                DrawBlock(x*16, y*16);
            }
        }
        GlobalRenderer->Next();
    }

    GlobalRenderer->Next();
    GlobalRenderer->PrintAtCursor("Kernel Panic: ");
    GlobalRenderer->PrintAtCursor(PanicMessage);
    GlobalRenderer->Next();
    GlobalRenderer->PrintAtCursor("We don't know what caused it.");
}